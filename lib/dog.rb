class Dog
    # setter method for name
    def name=(name)
      @name = name
    end
  
    # getter method
    def name
      @name
    end
  
    # setter method for breed
    def breed=(breed)
      @breed = breed
    end
  
    # getter method
    def breed
      @breed
    end
  end
 
  
  
  
  
  
  
  